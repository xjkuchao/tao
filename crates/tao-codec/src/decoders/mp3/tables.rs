/// 根据采样率获取 SFB 表索引
/// 0=11025/12000, 1=8000, 2=22050, 3=24000, 4=16000, 5=44100, 6=48000, 7=32000
pub fn samplerate_index(samplerate: u32) -> usize {
    match samplerate {
        11025 | 12000 => 0,
        8000 => 1,
        22050 => 2,
        24000 => 3,
        16000 => 4,
        44100 => 5,
        48000 => 6,
        32000 => 7,
        _ => 5,
    }
}

/// 构建 Long Blocks 的累积频带边界表
/// 返回 23 个值: sfb_bounds[i] = sum(widths[0..i])
/// sfb_bounds[0] = 0, sfb_bounds[22] = 576
pub fn build_sfb_long_bounds(samplerate: u32) -> [usize; 23] {
    let idx = samplerate_index(samplerate);
    let widths = &SFB_WIDTH_LONG[idx];
    let mut bounds = [0usize; 23];
    let mut acc = 0;
    for i in 0..22 {
        bounds[i] = acc;
        acc += widths[i];
    }
    bounds[22] = 576;
    bounds
}

/// 构建 Short Blocks 的累积频带边界表
/// 返回 14 个值
#[allow(dead_code)]
pub fn build_sfb_short_bounds(samplerate: u32) -> [usize; 14] {
    let idx = samplerate_index(samplerate);
    let widths = &SFB_WIDTH_SHORT[idx];
    let mut bounds = [0usize; 14];
    let mut acc = 0;
    for i in 0..13 {
        bounds[i] = acc;
        acc += widths[i];
    }
    bounds[13] = 192;
    bounds
}
